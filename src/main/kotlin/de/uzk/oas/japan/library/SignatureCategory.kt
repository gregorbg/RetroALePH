    fun computeHierarchy() = computeParentAncestry(this).reversed()

    companion object {
        fun searchSignature(sig: BookSignature.Standard): SignatureCategory? {
            val searchGroupIds = listOf(sig.category) + sig.subCategories
            return searchTree(searchGroupIds, sig.section) as? SignatureCategory
        }

        private tailrec fun searchTree(
            searchCriteria: List<Int>,
            currentNode: HierarchicalCategory
        ): HierarchicalCategory? {
            if (searchCriteria.isEmpty())
                return currentNode

            val childNodes = values().filter { it.parent == currentNode }

            val nextCriterion = searchCriteria.first()
            val candidateNodes = childNodes.filter { it.groupId == nextCriterion }

            if (candidateNodes.size != 1)
                return null

            return searchTree(searchCriteria.drop(1), candidateNodes.single())
        }

        private tailrec fun computeParentAncestry(
            node: HierarchicalCategory?,
            accu: List<HierarchicalCategory> = emptyList()
        ): List<HierarchicalCategory> {
            if (node == null)
                return accu

            return computeParentAncestry(node.parent, accu + node)
        }
    }
